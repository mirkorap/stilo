/// Defines line height constants
abstract class StiloLineHeight {
  /// Defines a line height of 1.0
  static const none = 1.0;

  /// Defines a line height of 1.25
  static const tight = 1.25;

  /// Defines a line height of 1.375
  static const snug = 1.375;

  /// Defines a line height of 1.5
  static const normal = 1.5;

  /// Defines a line height of 1.625
  static const relaxed = 1.625;

  /// Defines a line height of 2.0
  static const loose = 2.0;
}
