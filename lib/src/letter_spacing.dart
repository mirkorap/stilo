/// Defines letter spacing constants
abstract class StiloLetterSpacing {
  /// Defines a letter spacing of -0.6
  static const tighter = -0.6;

  /// Defines a letter spacing of -0.3
  static const tight = -0.3;

  /// Defines a letter spacing of 0
  static const normal = 0.0;

  /// Defines a letter spacing of 0.3
  static const wide = 0.3;

  /// Defines a letter spacing of 0.6
  static const wider = 0.6;

  /// Defines a letter spacing of 1.2
  static const widest = 1.2;
}
